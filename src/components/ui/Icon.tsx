interface IconProps {
  name: string;
  className?: string;
  size?: number;
  strokeWidth?: number;
}

export function Icon({ name, className, size = 16, strokeWidth = 1.5 }: IconProps) {
  const Glyph = registry[name] ?? Circle;

  return <Glyph aria-hidden size={size} strokeWidth={strokeWidth} className={className} />;
}
